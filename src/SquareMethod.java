/*
Problem 31: Square Method
Description: Write a method to return the square of a number.
Input: 4
Output: 16
*/
public class SquareMethod {
    static int square(int n) {
        return n * n;
    }

    public static void main(String[] args) {
        int num = 4;
        System.out.println(square(num));
    }
}
